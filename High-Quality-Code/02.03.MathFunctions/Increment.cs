using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _02._03.MathFunctions
{
    public class Increment
    {
        public static void IncrementInt(int endValue)
        {
            int i = 0;
            while (i < endValue)
            {
                i++;
            }
        }

        public static void IncrementLong(long endValue)
        {
            long i = 0;
            while (i < endValue)
            {
                i++;
            }
        }

        public static void IncrementFloat(float endValue)
        {
            float i = 0;
            while (i < endValue)
            {
                i++;
            }
        }

        public static void IncrementDouble(double endValue)
        {
            double i = 0;
            while (i < endValue)
            {
                i++;
            }
        }

        public static void IncrementDecimal(decimal endValue)
        {
            decimal i = 0;
            while (i < endValue)
            {
                i++;
            }
        }
    }
}
