//Declare  two integer variables and assign them with 5 and 10 and after that exchange their values
using System;

    class ExchangeValueOfIntegerNumbers
    {
        static void Main()
        {
            int a = 5; //declare first integer variable
            int b = 10; //declare second integer variable
            int c; //declare temporary variable
            
            //Exchange values of a and b
            c = a;
            a = b;
            b = c;
            // Console.WriteLine("a={0}, b={1}", a, b);
        }
    }

